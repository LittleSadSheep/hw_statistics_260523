# ============================================================
# 前缀去除：去掉选项标签前的 "A. " / "B. " 等
# ============================================================
import re

def strip_option_prefix(text: str) -> str:
    """移除 'A. 男' / 'B. 女' 等选项前缀，返回纯文本值"""
    if pd_isna(text):
        return text
    return re.sub(r'^[A-Z]+\.\s*', '', str(text)).strip()

# 延迟导入 pandas 的类型检查在此处理
def pd_isna(val):
    """检查是否为 NaN（避免循环导入 pandas）"""
    try:
        import pandas as pd
        return pd.isna(val)
    except ImportError:
        return val is None or (isinstance(val, float) and str(val) == 'nan')
